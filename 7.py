def on_one_line(p1, p2, p3): # проверка, лежат ли три точки на одной прямой
    x1, y1 = p1 # координаты первой точки
    x2, y2 = p2 # координаты второй точки
    x3, y3 = p3 # координаты третьей точки
    cross_product = (x2 - x1) * (y3 - y1) - (y2 - y1) * (x3 - x1) # вычисление векторного произведения
    return cross_product == 0 # точки лежат на одной прямой, если векторное произведение равно 0
print(on_one_line((1, 1), (2, 2), (3, 3)))
print(on_one_line((1, 1), (2, 2), (3, 4)))
print(on_one_line((1, 2), (2, 2), (3, 2)))
print(on_one_line((-1, -2), (-2, -2), (-3, -2)))
print(on_one_line((0, 0), (1, 1), (2, 2)))
print(on_one_line((1, 1), (0, 0), (-10, -10)))


